def percentFun(marks):
    p = ((marks[0] + marks[1] + marks[2] + marks[3])/400)*100
    return p

marks1 = [78, 58, 85, 65]
percentage1 = percentFun(marks1)
marks2 = [98, 96, 74, 85]
percentage2 = percentFun(marks2)
print(percentage1, percentage2)



